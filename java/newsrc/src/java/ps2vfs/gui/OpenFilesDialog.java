  private void resizeTable(java.awt.Component comp) {
    int width = comp.getWidth();
    int tableWidth = jOpenFilesTable.getWidth();
    //System.out.println("Width: " + width + " tableWidth: " + tableWidth);
    if(width > tableWidth) {
      TableColumn column = jOpenFilesTable.getColumnModel().getColumn(4);
      int lastColumnWidth = column.getWidth();
      if(false) {
	System.out.println("Adjusting last column with: " + 
			   (lastColumnWidth + (width - tableWidth)));
      }
      column.setWidth((lastColumnWidth + (width - tableWidth)));
      column.setPreferredWidth((lastColumnWidth + (width - tableWidth)));

      java.awt.Dimension d = jOpenFilesTable.getSize();
      d.width = d.width + (width - tableWidth);
      jOpenFilesTable.setSize(d);
    }
  }

