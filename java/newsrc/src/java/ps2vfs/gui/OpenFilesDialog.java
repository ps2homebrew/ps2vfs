  public void refreshModel(boolean keepColumnWidths) {
    int[] columnWidths = new int[5];
    TableColumn column = null;
    if(keepColumnWidths) {
      for (int i = 0; i < 5; i++) {
	column = jOpenFilesTable.getColumnModel().getColumn(i);
	if(column != null) {
	  columnWidths[i] = column.getPreferredWidth();
	} else {
	  columnWidths[i] = 100;
	}
      }
    }

    initModel();
    int totalColumnWidth = 0;
    if(keepColumnWidths) {
      for (int i = 0; i < 5; i++) {
	column = jOpenFilesTable.getColumnModel().getColumn(i);
	column.setPreferredWidth(columnWidths[i]);
	totalColumnWidth += columnWidths[i];
      }
    } else {
      for (int i = 0; i < 5; i++) {
	column = jOpenFilesTable.getColumnModel().getColumn(i);
	if (i == 0) {
	  column.setPreferredWidth(30);
	  column.setMinWidth(30);
	  totalColumnWidth += 30;
	} else if(i == 1) {
	  column.setPreferredWidth(50);
	  column.setMinWidth(50);
	  totalColumnWidth += 50;
	} else if(i == 4) {
	  column.setPreferredWidth(400);
	  column.setMinWidth(400);
	  totalColumnWidth += 400;
	} else {
	  column.setPreferredWidth(150);
	  column.setMinWidth(150);
	  totalColumnWidth += 150;
	}
      }
    }

    int width = jScrollPane1.getWidth();
    if(width > totalColumnWidth) {
      column = jOpenFilesTable.getColumnModel().getColumn(4);
      int lastColumnWidth = column.getWidth();
      column.setPreferredWidth(lastColumnWidth + (width - totalColumnWidth));
    }
  }

  private void initModel() {
    ps2vfs.vfs.VfsOpenFileInfo[] ofi = vfs.getOpenFilesInfo();
    Object [][] strTbl = null;
    if(ofi != null && ofi.length > 0) {
      strTbl = new Object[ofi.length][5];
      for(int r = 0; r < ofi.length; r++) {
	strTbl[r][0] = "" + ofi[r].getFD();
	strTbl[r][1] = ofi[r].getIPStr();
	strTbl[r][2] = ofi[r].getVPath();
	strTbl[r][3] = ofi[r].getOPath();
	String info = ofi[r].getInfo();
	strTbl[r][4] = info != null ? info : "";
      }
    }
    jOpenFilesTable.setModel(new javax.swing.table.DefaultTableModel(
				     strTbl,
				     new String [] {
				       "FileID", "Client", "Virtual path", "Actual path", 
				       "Info" }
				     ));
  }
  
